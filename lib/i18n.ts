export function t(key: string, locale: Locale = 'uz'): string {
  return translations[locale]?.[key] || translations.uz[key] || key
}

export function getLocale(): Locale {
  if (typeof window === 'undefined') return 'uz'
  try {
    const raw = localStorage.getItem('med-ai-auth')
    if (raw) {
      const parsed = JSON.parse(raw)
      if (parsed?.preferences?.language && translations[parsed.preferences.language as Locale]) {
        return parsed.preferences.language as Locale
      }
    }
  } catch { /* ignore */ }
  return 'uz'
}
